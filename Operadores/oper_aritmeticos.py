"""
Permite realizar operações matemáticas básicas entre valores numéricos.

+   (adição)
-   (subtração)
*   (multiplicação)
/   (divisão com resultado float)
//  (divisão inteira)
%   (resto da divisão)
**  (exponenciação)
"""

# Exemplo

a, b = 7, 3 
print(a + b)  # 10
print(a - b)  # 4
print(a * b)  # 21
print(a / b)  # 2.333...
print(a // b) # 2
print(a % b)  # 1
print(a ** b) # 343



